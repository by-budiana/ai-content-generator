const prisma = require('../lib/prisma');
const { z } = require('zod');

const generateSchema = z.object({
  prompt: z.string().min(5),
  type: z.enum(['CAPTION', 'DESCRIPTION', 'TAGLINE']),
  language: z.enum(['ID', 'EN']),
});

exports.generateContent = async (req, res) => {
  try {
    const validatedData = generateSchema.parse(req.body);
    const { prompt, type, language } = validatedData;

    // TODO: Integrate with AI Provider (OpenAI/Gemini)
    // For now, we use a mock response
    let mockResult = "";
    if (language === 'ID') {
      mockResult = `Hasil generate ${type} untuk prompt: "${prompt}". Ini adalah konten contoh yang dihasilkan oleh AI.`;
    } else {
      mockResult = `AI generated ${type} for prompt: "${prompt}". This is a sample content generated by AI.`;
    }

    const content = await prisma.content.create({
      data: {
        userId: req.user.id,
        inputPrompt: prompt,
        result: mockResult,
        type: type,
        language: language,
      },
    });

    res.status(201).json(content);
  } catch (error) {
    if (error instanceof z.ZodError) {
      return res.status(400).json({ errors: error.errors });
    }
    res.status(500).json({ message: error.message });
  }
};

exports.getHistory = async (req, res) => {
  try {
    const history = await prisma.content.findMany({
      where: { userId: req.user.id },
      orderBy: { createdAt: 'desc' },
      include: {
        ratings: {
          where: { userId: req.user.id }
        }
      }
    });
    res.json(history);
  } catch (error) {
    res.status(500).json({ message: error.message });
  }
};

exports.toggleFavorite = async (req, res) => {
  try {
    const { id } = req.params;
    const content = await prisma.content.findUnique({
      where: { id: parseInt(id) }
    });

    if (!content || content.userId !== req.user.id) {
      return res.status(404).json({ message: 'Content not found' });
    }

    const updatedContent = await prisma.content.update({
      where: { id: parseInt(id) },
      data: { isFavorite: !content.isFavorite }
    });

    res.json(updatedContent);
  } catch (error) {
    res.status(500).json({ message: error.message });
  }
};

exports.rateContent = async (req, res) => {
  try {
    const { id } = req.params;
    const { value } = req.body; // LIKE or DISLIKE

    if (!['LIKE', 'DISLIKE'].includes(value)) {
      return res.status(400).json({ message: 'Invalid rating value' });
    }

    const rating = await prisma.rating.upsert({
      where: {
        userId_contentId: {
          userId: req.user.id,
          contentId: parseInt(id)
        }
      },
      update: { value },
      create: {
        userId: req.user.id,
        contentId: parseInt(id),
        value
      }
    });

    res.json(rating);
  } catch (error) {
    res.status(500).json({ message: error.message });
  }
};

exports.deleteContent = async (req, res) => {
  try {
    const { id } = req.params;
    const content = await prisma.content.findUnique({
      where: { id: parseInt(id) }
    });

    if (!content || content.userId !== req.user.id) {
      return res.status(404).json({ message: 'Content not found' });
    }

    await prisma.content.delete({
      where: { id: parseInt(id) }
    });

    res.json({ message: 'Content deleted successfully' });
  } catch (error) {
    res.status(500).json({ message: error.message });
  }
};
